    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        
        
        
         if(jTable1.getSelectedRowCount()==1 && jTable2.getSelectedRowCount()==1){
             //Date cRdate = returndate.getDate();
             DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
             String Rdate = df.format(returndate.getDate());
             
             DefaultTableModel t1 = (DefaultTableModel) jTable1.getModel();
             String bookid = t1.getValueAt(jTable1.getSelectedRow(), 0).toString();
             String bookname = t1.getValueAt(jTable1.getSelectedRow(), 1).toString();
             
             DefaultTableModel t2 = (DefaultTableModel) jTable2.getModel();
             String memid = t2.getValueAt(jTable2.getSelectedRow(), 0).toString();
             String memname = t2.getValueAt(jTable2.getSelectedRow(), 1).toString();
             String num = t2.getValueAt(jTable2.getSelectedRow(), 4).toString();
             int yes = JOptionPane.showConfirmDialog(this, "Issue "+bookname+" To "+memname, "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
              if (yes == JOptionPane.YES_OPTION){
                  try {
                          FileWriter Writer = new FileWriter("issue.txt",true);
                        Writer.write(""+bookid+";;"+bookname+";;"+memid+";;"+memname+";;"+num+";;"+Rdate);
                       Writer.write(System.getProperty("line.separator"));
                      Writer.close();
            
                  setVisible(false);
                    new issue1().setVisible(true);
            
                   } catch (Exception e) {
                            JOptionPane.showMessageDialog(null, "Please Run The App As An Administrator");
        }
                  
              }
             
             
        
       
       
        }else{
            if(jTable1.getRowCount() == 0 || jTable2.getRowCount()==0){
                JOptionPane.showMessageDialog(this, "Empty Table...");
            }else {
                JOptionPane.showMessageDialog(this, "Please Select 1 Book and 1 Member..."+"\n OR check date!");
            }
            
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

