    private void jLabel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel5MouseClicked
        // TODO add your handling code here:
        
        
        
            
        
        
        try {
            FileReader fr = new FileReader("books.txt");
            FileReader frr = new FileReader("members.txt");
            
            
            BufferedReader br = new BufferedReader(fr);
            BufferedReader brr = new BufferedReader(frr);
            
            
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            DefaultTableModel model2 = (DefaultTableModel)jTable2.getModel();
            
            
            while(model.getRowCount()>0){
                for(int i=0;i<model.getRowCount();i++){
                    model.removeRow(i);
                }
            }
            while(model2.getRowCount()>0){
                for(int i=0;i<model2.getRowCount();i++){
                    model2.removeRow(i);
                }
            }
            
            
            Object[] lines =br.lines().toArray();
            Object[] lines2 =brr.lines().toArray();
            
            for (Object line : lines) {
                String[] row = line.toString().split(";;");
                model.addRow(row);
            }
            for(int i =0;i<lines2.length;i++){
                String[] row = lines2[i].toString().split(";;");
                model2.addRow(row);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(allbooks.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jLabel5MouseClicked

